import { writeFileSync, mkdirSync } from 'fs';
import { join } from 'path';
import type { BenchmarkResult } from '@resonance/core';

/**
 * Generate a markdown eval report and save to docs/evals/.
 */
export function writeMarkdownReport(results: BenchmarkResult[], projectRoot: string): string {
  const now = new Date();
  const dateStr = now.toISOString().split('T')[0];
  const timeStr = now.toISOString().split('T')[1].replace(/[:.]/g, '-').slice(0, 8);

  const testable = results.filter(r => r.target !== 'info');
  const passed = testable.filter(r => r.passed).length;
  const total = testable.length;
  const allPassed = passed === total;

  const lines: string[] = [
    `# Eval Report — ${dateStr}`,
    '',
    `**Timestamp:** ${now.toISOString()}`,
    `**Platform:** ${process.platform} ${process.arch}`,
    `**Node:** ${process.version}`,
    `**Result:** ${allPassed ? `${passed}/${total} PASS` : `${passed}/${total} — SOME FAILURES`}`,
    '',
    '---',
    '',
    '## Results',
    '',
    '| Metric | Target | Actual | Status |',
    '|--------|--------|--------|--------|',
  ];

  for (const r of results) {
    const status = r.target === 'info' ? 'INFO' : r.passed ? 'PASS' : 'FAIL';
    const icon = r.target === 'info' ? 'ℹ️' : r.passed ? '✅' : '❌';
    lines.push(`| ${r.name} | ${r.target} | ${r.actual} | ${icon} ${status} |`);
  }

  // Group details by category
  const perturbation = results.filter(r => r.name.startsWith('Perturbation'));
  const recall = results.filter(r => r.name.startsWith('Match recall'));
  const latency = results.filter(r =>
    r.name.includes('latency') || r.name.includes('Bootstrap') || r.name.includes('search')
  );

  lines.push('', '---', '', '## Analysis', '');

  // Perturbation summary
  if (perturbation.length > 0) {
    lines.push('### Privacy vs. Utility Tradeoff', '');
    lines.push('| Epsilon | Privacy Level | Cosine Similarity | Interpretation |');
    lines.push('|---------|---------------|-------------------|----------------|');
    for (const r of perturbation) {
      const d = r.details as Record<string, unknown>;
      const eps = d?.epsilon as number;
      const level = eps === 5.0 ? 'Low' : eps === 1.0 ? 'Medium' : 'High';
      const sim = (d?.meanSimilarity as number)?.toFixed(3) ?? r.value.toFixed(3);
      const interp = eps === 5.0
        ? 'Minimal noise — nearly identical to original'
        : eps === 1.0
          ? 'Moderate noise — signal preserved, privacy added'
          : 'Heavy noise — strong privacy, low utility';
      lines.push(`| ${eps} | ${level} | ${sim} | ${interp} |`);
    }
    lines.push('');
  }

  // Recall summary
  if (recall.length > 0) {
    lines.push('### Match Recall by Epsilon', '');
    lines.push('| Epsilon | Recall | Notes |');
    lines.push('|---------|--------|-------|');
    for (const r of recall) {
      const d = r.details as Record<string, unknown>;
      const eps = d?.epsilon;
      const rec = d?.recall as number;
      const pct = rec !== undefined ? `${(rec * 100).toFixed(1)}%` : r.actual;
      const note = eps === 'none'
        ? 'Baseline — no perturbation'
        : typeof eps === 'number' && eps >= 5.0
          ? 'Low privacy — matches preserved'
          : typeof eps === 'number' && eps >= 1.0
            ? 'Medium privacy — target >75%'
            : 'High privacy — significant degradation expected';
      lines.push(`| ${eps === 'none' ? '∞' : eps} | ${pct} | ${note} |`);
    }
    lines.push('');
  }

  // Latency summary
  if (latency.length > 0) {
    lines.push('### Performance', '');
    for (const r of latency) {
      const d = r.details as Record<string, unknown>;
      if (d && Object.keys(d).length > 0) {
        lines.push(`**${r.name}:** ${r.actual}`);
        const relevantKeys = ['p50', 'p95', 'p99', 'mean', 'min', 'max', 'memUsedMB'];
        const parts: string[] = [];
        for (const key of relevantKeys) {
          if (d[key] !== undefined) {
            const val = d[key] as number;
            const formatted = key === 'memUsedMB' ? `${val.toFixed(0)}MB` : `${val.toFixed(1)}ms`;
            parts.push(`${key}: ${formatted}`);
          }
        }
        if (parts.length > 0) {
          lines.push(`  - ${parts.join(', ')}`);
        }
        lines.push('');
      }
    }
  }

  // FPR detail
  const fpr = results.find(r => r.name.includes('False positive'));
  if (fpr) {
    const d = fpr.details as Record<string, unknown>;
    lines.push('### False Positive Rate', '');
    lines.push(`- **Rate:** ${fpr.actual}`);
    lines.push(`- **Relay threshold:** ${d?.relayThreshold}`);
    lines.push(`- **Confirmation threshold:** ${d?.confirmThreshold}`);
    lines.push(`- **Total matches evaluated:** ${d?.totalMatches}`);
    lines.push('');
  }

  lines.push('---', '', `*Generated by Resonance eval suite*`);

  const docsDir = join(projectRoot, 'docs', 'evals');
  mkdirSync(docsDir, { recursive: true });

  const filename = `eval-${dateStr}-${timeStr}.md`;
  const filepath = join(docsDir, filename);
  writeFileSync(filepath, lines.join('\n'));

  return filepath;
}
